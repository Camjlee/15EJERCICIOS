public class EJERCICIO1 {
    public static void main(String[] args) {

        int uno = 8;
        int dos = 4;
        int unoMasDos = uno + dos;
        int unoMenosDos = uno - dos;
        int unoPorDos = uno * dos;
        int unoDivididoDos = uno / dos;
        int modulo = uno % dos;

        System.out.printf("La suma de %d y %d es: %d \n", uno, dos, unoMasDos);

        System.out.printf("La resta de %d y %d es: %d \n", uno, dos, unoMenosDos);

        System.out.printf("El producto entre %d y %d es: %d \n", uno, dos, unoPorDos);

        System.out.printf("La división de %d en %d es: %d \n", uno, dos, unoDivididoDos);

        System.out.printf("El módulo entre %d y %d es: %d \n", uno, dos, modulo);



    }
}
